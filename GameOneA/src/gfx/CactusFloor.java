package gfx;

import java.awt.image.BufferedImage;

public class CactusFloor {

    private BufferedImage cactusfloor;

    public CactusFloor(BufferedImage cactusfloor){
        this.cactusfloor = cactusfloor;
    }
    public BufferedImage crop(int x, int y, int width, int height){
        return cactusfloor;
    }
}